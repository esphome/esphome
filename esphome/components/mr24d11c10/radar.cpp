unsigned short int us_CalculateCrc16(unsigned char *lpuc_Frame, unsigned short int lus_Len) {
  unsigned char luc_CRCHi = 0xFF;
  unsigned char luc_CRCLo = 0xFF;
  int li_Index = 0;
  while (lus_Len--) {
    li_Index = luc_CRCLo ^ *(lpuc_Frame++);
    luc_CRCLo = (unsigned char) (luc_CRCHi ^ cuc_CRCHi[li_Index]);
    luc_CRCHi = cuc_CRCLo[li_Index];
  }
  return (unsigned short int) (luc_CRCLo << 8 | luc_CRCHi);
}

char radar::CRC(char ad1, char ad2, char ad3, char ad4, char ad5, char ad6, char ad7) {
  unsigned char data[] = {ad1, ad2, ad3, ad4, ad5, ad6, ad7};
  unsigned short int crc_data = 0x0000;
  unsigned int lenth = sizeof(data) / sizeof(unsigned char);
  crc_data = us_CalculateCrc16(data, lenth);
  return crc_data;
}

typedef union {
  unsigned char Byte[4];
  float Float;
} Float_Byte;

int radar::Bodysign_val(int ad1, int ad2, int ad3, int ad4, int ad5) {
  if (ad1 == BODYSIGN) {
    Float_Byte fb;
    fb.Byte[0] = ad2;
    fb.Byte[1] = ad3;
    fb.Byte[2] = ad4;
    fb.Byte[3] = ad5;
    return fb.Float;
  }
  return 0;
}

int radar::Situation_judgment(int ad1, int ad2, int ad3, int ad4, int ad5) {
  int result = 0;
  if (ad1 == REPORT_RADAR || ad1 == REPORT_OTHER) {
    if (ad2 == ENVIRONMENT || ad2 == HEARTBEAT) {
      if (ad3 == NOBODY) {
        result = 1;
      } else if (ad3 == SOMEBODY_BE && ad4 == SOMEBODY_MOVE) {
        result = 2;
      } else if (ad3 == SOMEBODY_BE && ad4 == SOMEBODY_STOP) {
        result = 3;
      }
    } else if (ad2 == CLOSE_AWAY) {
      if (ad3 == CA_BE && ad4 == CA_BE) {
        if (ad5 == CA_BE) {
          result = 4;
        } else if (ad5 == CA_CLOSE) {
          result = 5;
        } else if (ad5 == CA_AWAY) {
          result = 6;
        }
      }
    }
  }
  return result;
}

int radar::Fall_judgment(int ad1, int ad2, int ad3, int ad4) {
  int result = 0;
  if (ad1 == FALL_REPORT && ad2 == 0x01) {
    if (ad3 == 0x01) {
      if (ad4 == 0x00) {
        result = 1;
      } else if (ad4 == 0x01) {
        result = 2;
      } else if (ad4 == 0x02) {
        result = 3;
      }
    } else if (ad3 == 0x02) {
      if (ad4 == 0x00) {
        result = 4;
      } else if (ad4 == 0x01) {
        result = 5;
      } else if (ad4 == 0x02) {
        result = 6;
      } else if (ad4 == 0x03) {
        result = 7;
      } else if (ad4 == 0x04) {
        result = 8;
      }
    }
  }
  return result;
}

